# Given an array of float numbers. Return the max value of them.

# Example:
# Input:  [1.0, 2.1, -3.3]
# Output: 2.1	
# Explanation: return the Max one.

class Solution:
    """
    @param A: An integer
    @return: a float number
    """
    def maxOfArray(self, A):
        # write your code here
        return max(A)